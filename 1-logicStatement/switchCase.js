/** switch case : adalah logic untuk memilih salah satu dari kondisi yang sudah di tentukan */

let remoteButton = 0;

switch (remoteButton) {
  case 1:
    console.log("Turn on");
    break;
  case 2:
    console.log("Turn off");
    break;
  case 3:
    console.log("volume up");
  case 4:
    console.log("volume down");
  default:
    console.log("input tidak valid");
    break;
}
